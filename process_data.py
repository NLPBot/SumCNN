    print('loading word2vec vectors...')
    w2v = load_bin_vec(w2v_file=w2v_file, vocab=None)
    cPickle.dump([w2v], open("word2vec.pkl", "wb"))
    print("word2vec loaded!")
    print("num words already in word2vec: " + str(len(w2v)))
